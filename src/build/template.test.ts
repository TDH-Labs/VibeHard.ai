import { afterEach, describe, expect, test } from "bun:test";
import { existsSync, mkdtempSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { TEMPLATES, applyTemplate, fixerTemplateBlock, isTemplateOwnedPath, persistWorkspaceTemplate, pickTemplate, readWorkspaceTemplate, templateBlock, templateDir } from "./template.ts";

describe("pickTemplate — the spec (not the stack string) decides", () => {
  test("clientOnlyStorage → the static client-only template", () => {
    expect(pickTemplate({ deployTarget: "hosted-app", clientOnlyStorage: true, storesData: true })?.key).toBe("next-static-client-only");
  });

  test("a hosted app that stores nothing → also static (no backend to justify Supabase)", () => {
    expect(pickTemplate({ deployTarget: "hosted-app", clientOnlyStorage: false, storesData: false })?.key).toBe("next-static-client-only");
  });

  test("a hosted app with server-side data → the Supabase template", () => {
    expect(pickTemplate({ deployTarget: "hosted-app", clientOnlyStorage: false, storesData: true })?.key).toBe("next-supabase");
  });

  test("a downloadable tool ships no hosted skeleton → null", () => {
    expect(pickTemplate({ deployTarget: "downloadable-tool", storesData: true })).toBeNull();
  });
});

describe("isTemplateOwnedPath — role-equivalents, not just exact names", () => {
  test("every spelling of an owned role is owned (a codegen next.config.js would SHADOW the template's next.config.mjs)", () => {
    for (const f of [
      "package.json",
      "./package.json",
      "/package.json",
      "package-lock.json",
      "yarn.lock",
      "tsconfig.json",
      "next.config.js",
      "next.config.mjs",
      "next.config.ts",
      "next-env.d.ts",
      "postcss.config.js",
      "postcss.config.cjs",
      "tailwind.config.js",
      "tailwind.config.ts",
      "app/layout.tsx",
      "src/app/layout.jsx",
      "app/globals.css",
      "styles/globals.css",
      "Dockerfile",
      ".dockerignore",
      ".gitignore",
      "README.md",
      "readme.md",
      "server.js",
      "server.ts",
      ".env.example",
    ]) {
      expect({ f, owned: isTemplateOwnedPath(f) }).toEqual({ f, owned: true });
    }
  });

  test("feature files are NOT owned — app/page.tsx is deliberately the feature surface", () => {
    for (const f of ["app/page.tsx", "app/timer/page.tsx", "components/Timer.tsx", "hooks/useTimer.ts", "lib/session.ts", "app/api/notes/route.ts", "public/logo.svg"]) {
      expect({ f, owned: isTemplateOwnedPath(f) }).toEqual({ f, owned: false });
    }
  });
});

describe("applyTemplate — vendored skeleton into the workspace", () => {
  const dirs: string[] = [];
  afterEach(() => {
    for (const d of dirs.splice(0)) rmSync(d, { recursive: true, force: true });
  });
  const dir = (): string => {
    const d = mkdtempSync(join(tmpdir(), "vibehard-tpl-"));
    dirs.push(d);
    return d;
  };

  test("copies the REAL vendored static template — complete and installable (manifest, lockfile, configs, layout, Dockerfile, server)", () => {
    const d = dir();
    const copied = applyTemplate(d, TEMPLATES["next-static-client-only"]);
    expect(copied).toBeGreaterThanOrEqual(TEMPLATES["next-static-client-only"].files.length);
    for (const f of TEMPLATES["next-static-client-only"].files) {
      expect({ f, exists: existsSync(join(d, f)) }).toEqual({ f, exists: true });
    }
    // The lockfile is REAL (generated by npm install, committed with the template) — the
    // depvuln gate needs it to assess transitives, and npm ci needs it to install at all.
    expect(readFileSync(join(d, "package-lock.json"), "utf8")).toContain('"lockfileVersion"');
  });

  test("the supabase template is complete too, and ships .env.example instead of server.js", () => {
    const d = dir();
    applyTemplate(d, TEMPLATES["next-supabase"]);
    for (const f of TEMPLATES["next-supabase"].files) {
      expect({ f, exists: existsSync(join(d, f)) }).toEqual({ f, exists: true });
    }
    expect(existsSync(join(d, "server.js"))).toBe(false);
  });

  test("never overwrites existing files (resume-safe; a themed tailwind.config.ts survives a re-apply), and a re-apply copies 0", () => {
    const d = dir();
    writeFileSync(join(d, "tailwind.config.ts"), "// themed by the design scaffold");
    applyTemplate(d, TEMPLATES["next-static-client-only"]);
    expect(readFileSync(join(d, "tailwind.config.ts"), "utf8")).toBe("// themed by the design scaffold");
    expect(applyTemplate(d, TEMPLATES["next-static-client-only"])).toBe(0);
  });

  test("a missing template directory throws loudly (a packaging bug, not a silently skeleton-less build)", () => {
    expect(() => applyTemplate(dir(), TEMPLATES["next-static-client-only"], "/nonexistent/tpl")).toThrow(/vendored templates/);
  });

  test("every template's owned-role files are actually matched by isTemplateOwnedPath (the boundary and the shipped files agree)", () => {
    for (const tpl of Object.values(TEMPLATES)) {
      for (const f of tpl.files) {
        if (f === "app/page.tsx" || f === "public/robots.txt") continue; // deliberately feature-owned / inert
        expect({ tpl: tpl.key, f, owned: isTemplateOwnedPath(f) }).toEqual({ tpl: tpl.key, f, owned: true });
      }
    }
  });

  test("the vendored template dirs exist in-repo (templateDir resolution)", () => {
    for (const tpl of Object.values(TEMPLATES)) expect(existsSync(templateDir(tpl.key))).toBe(true);
  });
});

describe("workspace template marker — the build's own record, read by later stages", () => {
  const dirs: string[] = [];
  afterEach(() => {
    for (const d of dirs.splice(0)) rmSync(d, { recursive: true, force: true });
  });
  const dir = (): string => {
    const d = mkdtempSync(join(tmpdir(), "vibehard-tplmark-"));
    dirs.push(d);
    return d;
  };

  test("persist → read round-trips to the same template", () => {
    const d = dir();
    persistWorkspaceTemplate(d, TEMPLATES["next-supabase"]);
    expect(readWorkspaceTemplate(d)?.key).toBe("next-supabase");
  });

  test("no marker (a pre-template build) → null; a malformed or unknown-key marker → null, never a throw", () => {
    expect(readWorkspaceTemplate(dir())).toBeNull();
    const d = dir();
    persistWorkspaceTemplate(d, TEMPLATES["next-static-client-only"]);
    writeFileSync(join(d, ".vibehard", "template.json"), "{not json");
    expect(readWorkspaceTemplate(d)).toBeNull();
    writeFileSync(join(d, ".vibehard", "template.json"), JSON.stringify({ key: "future-template" }));
    expect(readWorkspaceTemplate(d)).toBeNull();
  });
});

describe("fixerTemplateBlock — the fix loop's softer hands-off (e2e-9's fixer improvised a whole skeleton)", () => {
  test("permits minimal finding-directed edits but forbids wholesale rewrites, variant spellings, and a different port", () => {
    const b = fixerTemplateBlock(TEMPLATES["next-static-client-only"]);
    expect(b).toContain("ONLY when a finding explicitly points at it");
    expect(b).toContain("never rewrite it wholesale");
    expect(b).toContain("no pages/ directory");
    expect(b).toContain("process.env.PORT");
    expect(b).toContain("server.js");
  });
});

describe("templateBlock — codegen is told the skeleton exists", () => {
  test("names the hands-off boundary and the App Router feature surface", () => {
    const b = templateBlock(TEMPLATES["next-static-client-only"]);
    expect(b).toContain("PROJECT SKELETON ALREADY EXISTS");
    expect(b).toContain("app/page.tsx is yours to overwrite");
    expect(b).toContain("never use a pages/ directory");
    expect(b).toContain("PORT contract");
  });
});
